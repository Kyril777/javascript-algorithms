/* Print digit from 1 - 5 in ascending order.  */
for(let i=0; i<=5; i++){
   console.log(i);
}

/* Print digit from 1 - 5 in descending order.  */
for(let i=5; i>=0; i--){
   console.log(i);
}

/* Looping through the values of an array. */
const birds = ["canary", "osprey", "rook"];

for (const x of birds) {
  console.log(x)
}
